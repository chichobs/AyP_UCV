/*
Haga un algoritmo que tome como entrada 2 números distintos e imprime el segundo número mayor.
*/

#include <iostream>
using namespace std;

int main() {
    double V1, V2;
    
    //Solicitar los 2 números distintos 
    cout << "Ingrese el primer número (V1): ";
    cin >> V1;
    cout << "Ingrese el segundo número (V2, distinto de V1): ";
    cin >> V2;
    
    //Imprimir el segundo número mayor (el menor entre los 2)
    cout << "\nEl segundo número mayor es (el menor): ";
    
    //Determinar cual es el menor de los 2
    if (V1 < V2)
        cout << V1 << endl;
    else
        cout << V2 << endl;

    return 0;
}