/*
Haga un algoritmo que reciba como entrada un número entre 1 y 12 e imprima el nombre del mes correspondiente. Tome en cuenta posibles valores erróneos en la entrada.
*/

#include <iostream>

using namespace std;

int main() {
    int Mes;

    cout << "Ingrese el número del mes (1 a 12): ";
    cin >> Mes;

    cout << "El mes correspondiente es: ";

    if (Mes == 1) {
        cout << "Enero" << endl;
    } else if (Mes == 2) {
        cout << "Febrero" << endl;
    } else if (Mes == 3) {
        cout << "Marzo" << endl;
    } else if (Mes == 4) {
        cout << "Abril" << endl;
    } else if (Mes == 5) {
        cout << "Mayo" << endl;
    } else if (Mes == 6) {
        cout << "Junio" << endl;
    } else if (Mes == 7) {
        cout << "Julio" << endl;
    } else if (Mes == 8) {
        cout << "Agosto" << endl;
    } else if (Mes == 9) {
        cout << "Septiembre" << endl;
    } else if (Mes == 10) {
        cout << "Octubre" << endl;
    } else if (Mes == 11) {
        cout << "Noviembre" << endl;
    } else if (Mes == 12) {
        cout << "Diciembre" << endl;
    } else {
        cout << "ERROR: Valor fuera del rango (debe ser entre 1 y 12)" << endl;
    }

    return 0;
}